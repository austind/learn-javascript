// Semicolons separate statements

alert('Hello'); alert('World');

// Statements are usually written on separate lines

alert('Hello');
alert('World');

// Semicolons may be omitted in most cases when a line break exists.
// This is called an "implicit semicolon".

alert('Hello')
alert('World')

// There are cases when a semicolon might seem implicit, but actually isn't.
// These bugs are hard to find.

